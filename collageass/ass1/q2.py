# defind five state and it's capital using dictionary data type 1st print state then capitals.

states = { "state1" : "capital1" ,
          "state2" : "capital2" ,
          "state3 " : "capital3" ,
          "state4 " : "capital4 " ,
          "state5" : "capital5" }
print("sates : " )
for state in states.keys():
    print(state)
print("capitals : " )
for capitals in states.values():
    print(capitals)
